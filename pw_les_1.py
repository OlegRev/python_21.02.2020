"""
2. Пользователь вводит время в секундах. Переведите время в часы, минуты и
секунды и выведите в формате чч:мм:сс. Используйте форматирование строк.
"""

time_in_seconds = int(input('введите время в секундах: '))
seconds = time_in_seconds % 60
if seconds < 10:
    seconds = '0' + str(seconds)
minutes = (time_in_seconds % 60**2) // 60
if minutes < 10:
    minutes = '0' + str(minutes)
hours = time_in_seconds // 60**2
if hours < 10:
    hours = '0' + str(hours)

print(f'{hours}:{minutes}:{seconds}')

